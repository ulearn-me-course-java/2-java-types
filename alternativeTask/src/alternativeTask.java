public class alternativeTask {
    public static void main(String[] args)
    {
        /**
         * 98 + (76 + 5 + 4) / 32 = 100, так как результат храняется в int, только
         * целая часть нас интересует 76 + 5 + 4 / 32 = 2(2,53125)
         * И 98 + 2 = 100 :-)
         */
        System.out.println(98+(76+5+4)/32);
    }
}
